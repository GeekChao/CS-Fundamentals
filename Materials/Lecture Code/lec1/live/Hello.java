/* All Java files must declare a class.
 * The class must have the same name as this file.
*/
public class Hello {
    public static void main(String[] args) {
        System.out.println("Hello world!");
        /* Every variable in Java must have
         * a declared type before it can be
         * used. This choice is permanent.
         * TYPES ARE CHECKED BEFORE THE 
         * PROGRAM EVER RUNS! */
        int x;

        x = 5;
        x = x + 10;
        System.out.println(x);

        x = "Pizza";
    }
}

